"""
problem: binary to gray
"""
binary = input()
def BtoG(a):
    gray =a[0]
    for i in range(1,len(a)):
        if a[i-1] == a[i]:
            gray += "0"
        else:
            gray += "1"
    return gray
print(BtoG(binary))


